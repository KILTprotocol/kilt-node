	impl<T: Config<I>, I: 'static> Pallet<T, I> {
		pub fn add_association(
			sender: AccountIdOf<T>,
			did_identifier: DidIdentifierOf<T, I>,
			account: LinkableAccountId,
		) -> DispatchResult {
			let deposit = Deposit {
				owner: sender,
				amount: T::Deposit::get(),
			};
			let record = ConnectionRecord {
				deposit,
				did: did_identifier.clone(),
			};

			let is_unique_flag_enabled = <T as Config<I>>::UniqueLinkingEnabled::get();
			if is_unique_flag_enabled {
				let is_did_already_linked = ConnectedAccounts::<T, I>::iter_key_prefix(&did_identifier)
					.next()
					.is_some();
				ensure!(!is_did_already_linked, Error::<T, I>::LinkExisting);
			}

			LinkableAccountDepositCollector::<T, I>::create_deposit(
				record.clone().deposit.owner,
				record.deposit.amount,
			)?;
			<T as Config<I>>::BalanceMigrationManager::exclude_key_from_migration(
				&ConnectedDids::<T, I>::hashed_key_for(&account),
			);

			ConnectedDids::<T, I>::mutate(&account, |did_entry| -> DispatchResult {
				if let Some(old_connection) = did_entry.replace(record) {
					ConnectedAccounts::<T, I>::remove(&old_connection.did, &account);
					Self::deposit_event(Event::<T, I>::AssociationRemoved(account.clone(), old_connection.did));
					LinkableAccountDepositCollector::<T, I>::free_deposit(old_connection.deposit)?;
				}
				Ok(())
			})?;
			ConnectedAccounts::<T, I>::insert(&did_identifier, &account, ());
			Self::deposit_event(Event::AssociationEstablished(account, did_identifier));

			Ok(())
		}

		pub(crate) fn remove_association(account: LinkableAccountId) -> DispatchResult {
			if let Some(connection) = ConnectedDids::<T, I>::take(&account) {
				let is_key_migrated = <T as Config<I>>::BalanceMigrationManager::is_key_migrated(
					&ConnectedDids::<T, I>::hashed_key_for(&account),
				);

				if is_key_migrated {
					LinkableAccountDepositCollector::<T, I>::free_deposit(connection.deposit)?;
				} else {
					<T as Config<I>>::BalanceMigrationManager::release_reserved_deposit(
						&connection.deposit.owner,
						&connection.deposit.amount,
					)
				}

				ConnectedAccounts::<T, I>::remove(&connection.did, &account);
				Self::deposit_event(Event::AssociationRemoved(account, connection.did));
				Ok(())
			} else {
				Err(Error::<T, I>::NotFound.into())
			}
		}
	}

	pub(crate) struct LinkableAccountDepositCollector<T: Config<I>, I: 'static = ()>(PhantomData<(T, I)>);
	impl<T: Config<I>, I: 'static>
		StorageDepositCollector<AccountIdOf<T>, LinkableAccountId, <T as Config<I>>::RuntimeHoldReason>
		for LinkableAccountDepositCollector<T, I>
	{
		type Currency = T::Currency;
		type Reason = HoldReason<I>;

		fn reason() -> Self::Reason {
			HoldReason::Deposit
		}

		fn get_hashed_key(key: &LinkableAccountId) -> Result<sp_std::vec::Vec<u8>, DispatchError> {
			Ok(ConnectedDids::<T, I>::hashed_key_for(key))
		}

		fn deposit(
			key: &LinkableAccountId,
		) -> Result<Deposit<AccountIdOf<T>, <Self::Currency as Inspect<AccountIdOf<T>>>::Balance>, DispatchError> {
			let record = ConnectedDids::<T, I>::get(key).ok_or(Error::<T, I>::NotFound)?;
			Ok(record.deposit)
		}

		fn deposit_amount(_key: &LinkableAccountId) -> <Self::Currency as Inspect<AccountIdOf<T>>>::Balance {
			T::Deposit::get()
		}

		fn store_deposit(
			key: &LinkableAccountId,
			deposit: Deposit<AccountIdOf<T>, <Self::Currency as Inspect<AccountIdOf<T>>>::Balance>,
		) -> Result<(), DispatchError> {
			let record = ConnectedDids::<T, I>::get(key).ok_or(Error::<T, I>::NotFound)?;
			ConnectedDids::<T, I>::insert(key, ConnectionRecord { deposit, ..record });
			Ok(())
		}
	}
